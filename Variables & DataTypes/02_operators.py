a=3
b=4

#Arithmetic operators
print("the value of 3+4 is",a+b)
print("the value of 3-4 is",a-b)
print("the value of 3*4 is",a*b)
print("the value of 3/4 is",a/b)

#Assignment operators
a=34
a += 12
a -= 12
a *= 12
a /= 12
print(a)

# Comparison operators
b = (4>7)
b = (4<7)
b = (14==7)
b = (14!=7)
print(b)

# Logical operators
bool1 = True
bool2 = False
print("the value of bool1 & bool2 is",(bool1 and bool2))
print("the value of bool1 & bool2 is",(bool1 or bool2))
print("the value of bool1 & bool2 is",(not bool2))

